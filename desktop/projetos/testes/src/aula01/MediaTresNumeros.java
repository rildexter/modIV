package aula01;

public class MediaTresNumeros {
    public static void main(String[] args) {
        double numero1 = 9;
        double numero2 = 8.0;
        double numero3 = 10;

        double media = (numero1 + numero2 + numero3) / 3;

        System.out.println("A média dos três números é: " + media);
    }
}
